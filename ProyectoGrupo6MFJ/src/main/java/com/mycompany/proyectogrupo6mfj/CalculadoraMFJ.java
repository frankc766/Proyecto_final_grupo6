    private void jButton23ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton23ActionPerformed
        // boton %
        if(operaciones= true)
       {
           
         this.entrada.setNumero2(Double.parseDouble(this.pantalla.getText()));
             double n1=this.entrada.getNumero1();
            double n2=this.entrada.getNumero2();
          this.pantalla2.setText(decimalcero(n1)+
                               this.entrada.getFuncionmat()+n2+"% =");
          switch(this.entrada.getFuncionmat())
          {
            case "+" -> this.pantalla.setText(decimalcero(n1+(n1*n2/100)));
            case "-" -> this.pantalla.setText(decimalcero(n1-(n1*n2/100)));
            case "*" -> this.pantalla.setText(decimalcero(n1*n2/100));
              case "/" ->
              {
                  if(n2!=0)
                  {
                      this.pantalla.setText(decimalcero(n1/(n2/100)));
                   }
                  else
                  {
                    this.pantalla2.setText(this.pantalla2.getText()+"  ERROR");
                    this.pantalla.setText("");
                     this.entrada.setNumero1(0);
                    this.entrada.setFuncionmat("0");
                    }
              }
           }
       }
        
    }//GEN-LAST:event_jButton23ActionPerformed

