//funcion igual
    public String funcioigual(){
      this.entrada.setNumero2(Double.parseDouble(this.pantalla.getText()));
        this.pantalla2.setText(decimalcero(this.entrada.getNumero1())+
                               this.entrada.getFuncionmat()+this.pantalla.getText()+"=");
        switch(this.entrada.getFuncionmat()){
            case "+" -> this.pantalla.setText(decimalcero(this.entrada.getNumero1()+
                                                            this.entrada.getNumero2()));
            case "-" -> this.pantalla.setText(decimalcero(this.entrada.getNumero1()-
                                                            this.entrada.getNumero2()));
            case "*" -> this.pantalla.setText(decimalcero(this.entrada.getNumero1()*
                                                             this.entrada.getNumero2()));
              case "/" -> dividirentrecero();
              
              case "Exp"->this.pantalla.setText(decimalcero(this.entrada.getNumero1()*
                                                             Math.pow(10,this.entrada.getNumero2())));
              
              case "√"->this.pantalla.setText(decimalcero(Math.pow(this.entrada.getNumero2(),
                                                                     (1/this.entrada.getNumero1()))));
              
            case "^"->this.pantalla.setText(decimalcero(Math.pow(this.entrada.getNumero1(),
                                                                     this.entrada.getNumero2())));
        }
        return null;
    
    }

//quitar punto decimal cuando sea 0.0 ^
    public String decimalcero(double total){
        var totalfin ="";
        totalfin=Double.toString(total);
        if(total%1==0){
            totalfin=totalfin.substring(0,totalfin.length()-2);
        }
        return totalfin;
    }
